    public function countBattles($PlayerID)
    {
        $a = $this->createQueryBuilder('u')
            ->select('ub.id')
            ->innerJoin('u.PlayerBattles', 'ub')
            ->where('u.id=:bid')
            ->setParameter('bid', $PlayerID);
        $b = $a->getQuery();
        return count($b->getArrayResult());
    }
